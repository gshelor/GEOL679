def glc1(a, b):
    """
    This program will take arguments a, b.
    It will return a * b, a + b.
    """  
    c = a * b
    d = a + b
    return c, d

def glc2(a, b):
    """
    This program will take arguments a, b.
    It will return a / b, a - b.
    """  
    c = a / b
    d = a - b
    return c, d

